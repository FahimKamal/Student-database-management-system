    private void registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerActionPerformed
        // TODO add your handling code here:
        if(name.getText().equals("Enter name") || phone.getText().equals("Enter Phome number") || userName.getText().equals("Create user name") || newPassword.getPassword().equals("New password") || confirmPassword.getPassword().equals("Confirm password")){            
            JOptionPane.showMessageDialog(this,"You haven't filled all the fields.", "Failure", JOptionPane.ERROR_MESSAGE);
        }
        else{
            if(!(String.valueOf(newPassword.getPassword()).equals(String.valueOf(confirmPassword.getPassword())))){
                JOptionPane.showMessageDialog(this,"Password doesn't match!!!", "Failure", JOptionPane.ERROR_MESSAGE);
            }
            else{
                int dialogButton = JOptionPane.YES_NO_OPTION;
                int dialogResult = JOptionPane.showConfirmDialog (this, "Are you sure you want to submit?","Warning",dialogButton);
                if(dialogResult == JOptionPane.YES_OPTION){
                    try{
                        Class.forName("com.mysql.cj.jdbc.Driver");
                        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/cse?serverTimezone=EST5EDT", "root", "");
                        String sql = "insert into user_info values (?, ?, ?, ?)";
                        PreparedStatement p = conn.prepareStatement(sql);
                        
                        p.setString(1, userName.getText());
                        p.setString(2, name.getText());
                        p.setString(3, phone.getText());
                        p.setString(4, String.valueOf(newPassword.getPassword()));
                        p.executeUpdate();
                        JOptionPane.showMessageDialog(this,"Registration Successful.", "Confirmation", JOptionPane.INFORMATION_MESSAGE);
                        updateComboBox();
                        cancel.doClick();                        
                    }catch(Exception ex){ JOptionPane.showMessageDialog(this,ex); }
                }
            }
            
        }
    }//GEN-LAST:event_registerActionPerformed

