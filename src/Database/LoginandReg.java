    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        // TODO add your handling code here:
        if(selectUser.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(this,"You haven't selected any account.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else if(selectUser.getSelectedIndex() != 0 && String.valueOf(password.getPassword()).equals("Enter password")){
            JOptionPane.showMessageDialog(this,"Please enter password.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            String pass = "";
            try{
                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/cse?serverTimezone=EST5EDT", "root", "");
                String sql = "SELECT * FROM `user_info` WHERE `User_name` = '"+selectUser.getItemAt(selectUser.getSelectedIndex())+"'";
                PreparedStatement p = conn.prepareStatement(sql);
                ResultSet rs = p.executeQuery();
                if(rs.next()){
                    pass = rs.getString("Password");
                }                
            }catch(Exception ex){ JOptionPane.showMessageDialog(this,ex); }
            if(pass.equals(String.valueOf(password.getPassword()))){
                JOptionPane.showMessageDialog(this, "Welcome "+selectUser.getItemAt(selectUser.getSelectedIndex()),"Login Successful.", JOptionPane.INFORMATION_MESSAGE);
                new homepage().setVisible(true);
                dispose();
            }
            else{ JOptionPane.showMessageDialog(this,"Wrong Password!!", "Error", JOptionPane.ERROR_MESSAGE); }
        }
    }//GEN-LAST:event_loginActionPerformed

