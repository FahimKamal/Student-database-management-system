    private void recover_passwordMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_recover_passwordMouseClicked
        // TODO add your handling code here:
        if(user.getText().equals("Enter user name") || user.getText().equals("")){
            JOptionPane.showMessageDialog(this,"You haven't entered any user name.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            try{
                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/cse?serverTimezone=EST5EDT", "root", "");
                String sql = "SELECT * FROM `user_info` WHERE `User_name` = '"+ user.getText() +"'";
                PreparedStatement p = conn.prepareStatement(sql);
                ResultSet rs = p.executeQuery();
                if(rs.next()){
                    dispose();
                    new recoverPassword(user.getText()).setVisible(true);                     
                }
                else{ JOptionPane.showMessageDialog(this,"User doesn't exist!!", "Error", JOptionPane.ERROR_MESSAGE); return; }
            }catch(Exception ex){ JOptionPane.showMessageDialog(this,ex); }           
        }        
    }//GEN-LAST:event_recover_passwordMouseClicked

