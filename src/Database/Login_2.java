    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        // TODO add your handling code here:
        if(user.getText().equals("Enter user name")){
            JOptionPane.showMessageDialog(this,"You haven't entered any user name.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else if(!(user.getText().equals("Enter user name")) && String.valueOf(password.getPassword()).equals("Enter password")){
            JOptionPane.showMessageDialog(this,"Please enter password.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            String pass = "", adminUser = "";
            try{
                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/cse?serverTimezone=EST5EDT", "root", "");
                String sql = "SELECT * FROM `user_info` WHERE `User_name` = '"+ user.getText() +"'";
                PreparedStatement p = conn.prepareStatement(sql);
                ResultSet rs = p.executeQuery();
                if(rs.next()){
                    pass = rs.getString("Password");
                    adminUser = rs.getString("admin_user");
                }                
                else{ JOptionPane.showMessageDialog(this,"Wrong User!!", "Error", JOptionPane.ERROR_MESSAGE); return; }
                conn.close();
            }catch(Exception ex){ JOptionPane.showMessageDialog(this,ex); }
            
            if(checkpw(String.valueOf(password.getPassword()), pass)){
                JOptionPane.showMessageDialog(this, "Welcome " + user.getText() ,"Login Successful.", JOptionPane.INFORMATION_MESSAGE);
                Date now = new Date();
                SimpleDateFormat dateFormatter = new SimpleDateFormat("EEEE, MMMM d, yyyy 'at' hh:mm:ss a zzz");
                String date = dateFormatter.format(now);
                try{
                    Class.forName("com.mysql.cj.jdbc.Driver");                
                    Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/cse?serverTimezone=EST5EDT", "root", "");
                    
                    String query = "UPDATE `user_info` SET `Last_login`='"+ date +"' WHERE `User_name` = '"+user.getText()+"'";
                    PreparedStatement pstmt = conn.prepareStatement(query);
                    pstmt.executeUpdate();            
                }catch(Exception ex){ JOptionPane.showMessageDialog(this,ex); }
                new homepage(user.getText(), adminUser).setVisible(true);
                dispose();
            }
            else{ JOptionPane.showMessageDialog(this,"Wrong Password!!", "Error", JOptionPane.ERROR_MESSAGE); }
        }
    }//GEN-LAST:event_loginActionPerformed

