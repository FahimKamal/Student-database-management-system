    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        if(dept.getSelectedIndex() == 0 && batch.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(this,"You haven't selected any data yet.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog (this, "Are you sure you want to delete?\nThere is no way to recover this data.","Warning",dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION){
                try{
                    Class.forName("com.mysql.cj.jdbc.Driver");
                    String database = dept.getItemAt(dept.getSelectedIndex());
                    String table = getBackupTable(batch.getItemAt(batch.getSelectedIndex()));
                    Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/" + database + "?serverTimezone=EST5EDT", "root", "");
                    
                    int i = datatable.getSelectedRow();
                    TableModel model = datatable.getModel();
                    String index = model.getValueAt(i, 0).toString();
                    
                    String query = "DELETE FROM `"+table+"` WHERE `Serial NO.` = " +index;
                    PreparedStatement pstmt = conn.prepareStatement(query);
                    pstmt.executeUpdate();
                    JOptionPane.showMessageDialog(this,"Operation Successful.");
                                        
                    //  Set the Detail Information fields empty
                    name.setText("");
                    dateOfBirth.setText("");
                    gender.setText("");
                    semester.setText("");
                    ID.setText("");
                    phone.setText("");
                    email.setText("");
                    
                    show_user(database,table);
                    
                }catch(Exception e){ JOptionPane.showMessageDialog(this,e); }
            }
        }
                        
    }//GEN-LAST:event_deleteActionPerformed

