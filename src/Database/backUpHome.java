    public ArrayList<User> userList(String database, String table){
        ArrayList<User> userList = new ArrayList<>();
        
        try{
            //Class.forName("com.mysql.jdbc.Driver");
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/"+database+"?serverTimezone=EST5EDT", "root", "");
                    
            String query = "SELECT * FROM " + table;
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(query);
            
            User user;
            
            while(rs.next()){
                user = new User(rs.getInt("Serial NO."), decrypt(rs.getString("Name"), secretKey), rs.getString("ID"));
                userList.add(user);
            }
            return userList;
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,e);
        }
        return userList;
    }
    
